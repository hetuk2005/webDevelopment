/* Arithmethic  Operators */

/* 

1. + - Addition
2. - - Subtraction
3. * - Multiplication
4. / - Division
5. % - Modules (Remainder)
6. ** - Exponentional (Eg:- 2**3=8)
7. ++ - Increament
8. -- - Decrement

*/

//Using Basic Maths:-

let num1 = 4;
let num2 = 2;

console.log(num1 + num2);
console.log(num1 - num2);
console.log(num1 * num2);
console.log(num1 / num2);
console.log(num1 ** num2);
console.log(num1 % num2);
console.log(num1++);
console.log(num1--);
console.log(num2++);
console.log(num2--);